sub ribbon_error {
    my $error_code    = shift;
    my @params        = @_;

    ## Parameter reference numbers start at 1, so unshift with an 'empty' value
    unshift( @params, '$params[0]');

    for my $i ( 1 .. 10 ) {
        next if ( $params[$i]);
        $params[$i] = "\%$i\%";
    };

    my $error_map     = error_map( @params );
    my $unknown       = "Error code $error_code is unknown";
    my $error_message = $error_map->{$error_code} // $unknown;

    return $error_message;
}

